export function modelPageName(pgName){    

    try {
      eval(pgName);  
      $("#app").html(eval(pgName));

    } catch (e) {
      console.log("Hello ", e);
    }

    if(pgName == "homeContent"){
      $("nav").removeClass("black-text").addClass("white-text");
    }else{
      $("nav").removeClass("white-text").addClass("black-text");
    }
}

